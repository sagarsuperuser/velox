package domain

import "time"

type CreditEntryType string

const (
	CreditGrant      CreditEntryType = "grant"
	CreditUsage      CreditEntryType = "usage"
	CreditExpiry     CreditEntryType = "expiry"
	CreditAdjustment CreditEntryType = "adjustment"
)

type CreditLedgerEntry struct {
	ID           string          `json:"id"`
	TenantID     string          `json:"tenant_id,omitempty"`
	CustomerID   string          `json:"customer_id"`
	EntryType    CreditEntryType `json:"entry_type"`
	AmountCents  int64           `json:"amount_cents"`
	BalanceAfter int64           `json:"balance_after"`
	Description  string          `json:"description"`
	InvoiceID    string          `json:"invoice_id,omitempty"`
	ExpiresAt    *time.Time      `json:"expires_at,omitempty"`
	Metadata     map[string]any  `json:"metadata,omitempty"`
	CreatedAt    time.Time       `json:"created_at"`
	// ConsumedCents tracks how much of a positive block's amount has
	// been RETIRED — drained by FIFO-attributed usage/clawback entries
	// plus the portion written off by expiry (Orb's credit-block
	// model). Meaningful only for positive blocks (grants, positive
	// adjustments, reversals); 0 for usage / expiry rows themselves.
	// Block remaining = amount_cents - consumed_cents; consumed_cents
	// == amount_cents means the block is exhausted or expired and is
	// structurally excluded from draining and from the expiry sweep.
	// Expiry flips consumed_cents to amount_cents atomically with its
	// -remaining ledger entry (ExpireGrantAtomic, ADR-071), so a
	// backdated apply can never re-drain a retired grant.
	ConsumedCents int64 `json:"consumed_cents,omitempty"`

	// SourceSubscriptionID + SourceSubscriptionItemID + SourcePlanChangedAt +
	// SourceChangeType, when all set, identify this entry as a proration
	// credit generated by an item mutation (downgrade plan change, qty
	// reduction, item remove). Combined with TenantID they form the natural
	// key used to dedup retries of proration grant — see migrations 0026 +
	// 0027 (item id + change_type disambiguate simultaneous mutations of
	// different kinds that share a wall-clock timestamp).
	SourceSubscriptionID     string         `json:"source_subscription_id,omitempty"`
	SourceSubscriptionItemID string         `json:"source_subscription_item_id,omitempty"`
	SourcePlanChangedAt      *time.Time     `json:"source_plan_changed_at,omitempty"`
	SourceChangeType         ItemChangeType `json:"source_change_type,omitempty"`

	// SourceCreditNoteID dedups grants created by credit-note Issue().
	// Set on every grant the CN refund-routing path appends; the
	// partial unique index idx_credit_ledger_credit_note_dedup
	// (migration 0093) enforces one grant per (tenant, CN). A retry
	// after partial-failure of CN Issue() hits the index, store
	// returns ErrAlreadyExists, service fetches the existing grant.
	SourceCreditNoteID string `json:"source_credit_note_id,omitempty"`

	// SourceInvoiceReversalID dedups invoice-void credit reversals. credit.
	// ReverseForInvoice (invoice void / dunning manual-resolve) stamps the
	// voided invoice's id here; the partial unique index
	// idx_credit_ledger_reversal_dedup (migration 0106) enforces one reversal
	// grant per (tenant, invoice). A second void/resolve of the same invoice
	// hits the index, the store returns ErrAlreadyExists, and ReverseForInvoice
	// treats it as an idempotent no-op instead of double-crediting.
	SourceInvoiceReversalID string `json:"source_invoice_reversal_id,omitempty"`

	// GrantKind classifies a grant's cost basis (ADR-078). 'commit' =
	// purchased prepaid commit (funded by a real invoice); 'promotional' =
	// explicitly-marked free/marketing credit, drained BEFORE all paid-class
	// blocks (zero-cost-basis-first). Empty/NULL = legacy/unclassified
	// (proration credits, CN grants, goodwill) — money-derived liabilities
	// that drain in the paid class with commits. Only drain order and
	// reporting read this tag; balance math is kind-blind.
	GrantKind GrantKind `json:"grant_kind,omitempty"`

	// SourceInvoiceID links a commit grant to the invoice that funds it
	// (ADR-078). Distinct from InvoiceID (stamped by usage/reversal entries,
	// many rows per invoice). The partial unique index
	// idx_credit_ledger_commit_fund_dedup (migration 0136) enforces one
	// commit grant per (tenant, funding invoice) — a structural backstop
	// behind the finalize CAS, whose violation inside the finalize
	// coordinator tx is a broken invariant and aborts loudly (never caught
	// in-tx: poisoned-tx).
	SourceInvoiceID string `json:"source_invoice_id,omitempty"`
}

// GrantKind is the cost-basis class of a credit grant (ADR-078).
type GrantKind string

const (
	// GrantKindCommit marks a purchased prepaid commit, funded by a real
	// invoice (source_invoice_id set). Drains in the paid class.
	GrantKindCommit GrantKind = "commit"
	// GrantKindPromotional marks an explicitly-free credit (marketing
	// spend). Drains before every paid-class block.
	GrantKindPromotional GrantKind = "promotional"
)

type CreditBalance struct {
	CustomerID   string `json:"customer_id"`
	BalanceCents int64  `json:"balance_cents"`
	TotalGranted int64  `json:"total_granted"`
	TotalUsed    int64  `json:"total_used"`
	TotalExpired int64  `json:"total_expired"`
}
