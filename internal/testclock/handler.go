// AuditWriter is the narrow audit surface testclock uses.
type AuditWriter interface {
	Log(ctx context.Context, tenantID, action, resourceType, resourceID, resourceLabel string, metadata map[string]any) error
}

// Handler exposes /v1/test-clocks. Auth middleware upstream enforces that the
// caller holds a test-mode key (livemode=false); the service itself adds a
// second guard so a misconfigured auth chain can't slip live writes through.
type Handler struct {
	svc         *Service
	auditLogger AuditWriter
}

func NewHandler(svc *Service) *Handler {
	return &Handler{svc: svc}
}

// SetAuditLogger wires audit on test-clock lifecycle. Critical for
// forensics: "was this invoice generated by a real cycle or by an
// operator advancing a sim clock?" is answered by cross-referencing
// the clock advance audit row with the invoice's issued_at.
func (h *Handler) SetAuditLogger(a AuditWriter) {
	h.auditLogger = a
}

func (h *Handler) Routes() chi.Router {
	r := chi.NewRouter()
	r.Use(h.requireTestMode)
	r.Post("/", h.create)
	r.Get("/", h.list)
	r.Get("/{id}", h.get)
	r.Get("/{id}/subscriptions", h.listSubscriptions)
	r.Get("/{id}/customers", h.listAttachedCustomers)
	r.Post("/{id}/advance", h.advance)
	r.Post("/{id}/retry-advance", h.retryAdvance)
	r.Delete("/{id}", h.delete)
	return r
}

// requireTestMode short-circuits requests from live-mode keys with 403. The
// underlying table enforces livemode=false via CHECK, so a live call would
// eventually fail anyway — this makes the failure immediate and explains why.
func (h *Handler) requireTestMode(next http.Handler) http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		if auth.Livemode(r.Context()) {
			respond.Forbidden(w, r, "test clocks are only available in test mode — use a test-mode API key (vlx_secret_test_...)")
			return
		}
		next.ServeHTTP(w, r)
	})
}

func (h *Handler) create(w http.ResponseWriter, r *http.Request) {
	tenantID := auth.TenantID(r.Context())

	var input CreateInput
	if err := json.NewDecoder(r.Body).Decode(&input); err != nil {
		respond.BadRequest(w, r, "invalid JSON body")
		return
	}

	clk, err := h.svc.Create(r.Context(), tenantID, input)
	if err != nil {
		respond.FromError(w, r, err, "test_clock")
		return
	}
	if h.auditLogger != nil {
		_ = h.auditLogger.Log(h.auditCtx(r.Context(), clk), tenantID, domain.AuditActionCreate, "test_clock", clk.ID, clk.Name, map[string]any{
			"frozen_time": clk.FrozenTime,
		})
	}
	respond.JSON(w, r, http.StatusCreated, clk)
}

// auditCtx puts a test_clock row ON ITS OWN CLOCK'S sim axis: the clock is
// both the resource and the pin, so sim_effective_at is the frozen instant the
// clock stood at when the operator acted.
//
// This is what makes the clock filter self-contained after ADR-086 teardown.
// Teardown hard-deletes every simulated business row, so a clock-scoped
// forensic view is reconstructed entirely from audit_log — and without these
// rows it would have the effects (invoices finalized, subs canceled) but not
// the CAUSE: no "created", no "advanced to 2027-03-01", and no "deleted", which
// is the row that explains why every other trace of the simulation is gone.
func (h *Handler) auditCtx(ctx context.Context, clk domain.TestClock) context.Context {
	return clock.WithSim(ctx, clock.Sim{At: clk.FrozenTime, TestClockID: clk.ID})
}

func (h *Handler) list(w http.ResponseWriter, r *http.Request) {
	tenantID := auth.TenantID(r.Context())

	clocks, err := h.svc.List(r.Context(), tenantID)
	if err != nil {
		respond.InternalError(w, r)
		slog.ErrorContext(r.Context(), "list test clocks", "error", err)
		return
	}
	if clocks == nil {
		clocks = []domain.TestClock{}
	}
	respond.JSON(w, r, http.StatusOK, map[string]any{"data": clocks})
}

func (h *Handler) get(w http.ResponseWriter, r *http.Request) {
	tenantID := auth.TenantID(r.Context())
	id := chi.URLParam(r, "id")

	clk, err := h.svc.Get(r.Context(), tenantID, id)
	if err != nil {
		respond.FromError(w, r, err, "test_clock")
		return
	}
	respond.JSON(w, r, http.StatusOK, clk)
}

