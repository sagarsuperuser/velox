func (h *Handler) advance(w http.ResponseWriter, r *http.Request) {
	tenantID := auth.TenantID(r.Context())
	id := chi.URLParam(r, "id")

	var input AdvanceInput
	if err := json.NewDecoder(r.Body).Decode(&input); err != nil {
		respond.BadRequest(w, r, "invalid JSON body")
		return
	}

	// Read the clock BEFORE advancing so the audit row can record the WINDOW the
	// advance swept, not just where it landed. Without the "from" instant, an
	// advance row says "the clock is now at 2027-06-01" and nothing else — so the
	// events it caused (every invoice, cancel and dunning step settled in that
	// sweep) cannot be bounded to it. After ADR-086 teardown these rows are the
	// ONLY surviving record of the simulation, and "which advance produced this?"
	// is precisely the question they exist to answer.
	//
	// Best-effort: a read failure must not block the operator's advance. The row
	// then carries the destination alone, exactly as it always did.
	var advancedFrom *time.Time
	if before, gerr := h.svc.Get(r.Context(), tenantID, id); gerr == nil {
		from := before.FrozenTime
		advancedFrom = &from
	}

	clk, err := h.svc.Advance(r.Context(), tenantID, id, input)
	if err != nil {
		respond.FromError(w, r, err, "test_clock")
		return
	}
	if h.auditLogger != nil {
		meta := map[string]any{
			"action":      "advanced",
			"frozen_time": clk.FrozenTime,
		}
		if advancedFrom != nil {
			meta["advanced_from"] = *advancedFrom
		}
		_ = h.auditLogger.Log(h.auditCtx(r.Context(), clk), tenantID, domain.AuditActionUpdate, "test_clock", clk.ID, clk.Name, meta)
	}
	respond.JSON(w, r, http.StatusOK, clk)
}

// retryAdvance resumes a clock parked at status='internal_failure'
// after a prior catchup error. The catchup loop is idempotent on
// subs whose next_billing_at <= frozen_time, so resuming from
// where the previous attempt stopped is safe by construction.
// 200 with the clock now in 'advancing' on success; 409 when the
// clock isn't in internal_failure (refuses to retry from ready
// or advancing). ADR-018.
func (h *Handler) retryAdvance(w http.ResponseWriter, r *http.Request) {
	tenantID := auth.TenantID(r.Context())
	id := chi.URLParam(r, "id")

	clk, err := h.svc.RetryAdvance(r.Context(), tenantID, id)
	if err != nil {
		respond.FromError(w, r, err, "test_clock")
		return
	}
	if h.auditLogger != nil {
		_ = h.auditLogger.Log(h.auditCtx(r.Context(), clk), tenantID, domain.AuditActionUpdate, "test_clock", clk.ID, clk.Name, map[string]any{
			"action":      "retry_advance",
			"frozen_time": clk.FrozenTime,
		})
	}
	respond.JSON(w, r, http.StatusOK, clk)
}

func (h *Handler) delete(w http.ResponseWriter, r *http.Request) {
	tenantID := auth.TenantID(r.Context())
	id := chi.URLParam(r, "id")

	// Read the clock BEFORE the teardown: after Delete it does not exist, and
	// this row is the one that has to survive it. ADR-086 teardown hard-deletes
	// the clock's entire simulated customer graph, so "deleted TC-x, frozen at
	// 2027-03-01, N customers" is the last thing the audit log can say about the
	// simulation — and the reason a clock-scoped query returns nothing else.
	// A failed read costs the sim axis on this row, never the deletion.
	auditCtx := r.Context()
	label := ""
	if clk, err := h.svc.Get(r.Context(), tenantID, id); err == nil {
		auditCtx = h.auditCtx(auditCtx, clk)
		label = clk.Name
	}

	if err := h.svc.Delete(r.Context(), tenantID, id); err != nil {
		respond.FromError(w, r, err, "test_clock")
		return
	}
	if h.auditLogger != nil {
		_ = h.auditLogger.Log(auditCtx, tenantID, domain.AuditActionDelete, "test_clock", id, label, map[string]any{
			"action": "teardown",
		})
	}
	respond.JSON(w, r, http.StatusOK, map[string]string{"status": "deleted"})
}
